"""
封包”通常指的是将多个值打包（pack）成一个整体，
常见的方式是用元组（tuple）或其他数据结构把多个变量合并在一起。
这样可以方便地一次性传递、返回或存储多个数据。
"""
a = 1
b = 2
c = 3
t = (a, b, c)  # 这里就是封包，把a、b、c打包成一个元组t

'''
在函数参数和返回值中也经常用到封包：
参数封包：用args和kwargs把多个参数打包成元组或字典。
返回值封包：函数可以一次性返回多个值，实际上是把它们封包成一个元组。
'''
def foo(*args):
    print(args)  # args是一个元组，封包了所有的位置参数

def bar():
    return 1, 2, 3  # 实际返回的是一个元组 (1, 2, 3)

'''
总结：
封包就是把多个独立的值“打包”成一个整体（如元组、列表、字典等），
方便统一处理和传递。与之相对的概念是“解包”，即把整体拆分成单独的值。
'''