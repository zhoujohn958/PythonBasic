"""
定义：命名空间（Namespace）是一个从名称到对象的映射。
简单来说，就是一个容器，用于存放变量名（标识符）与其对应的对象（值）之间的关系
"""
'''
内置命名空间（Built-in Namespace）
由 Python 解释器在启动时创建，包含了 Python 内置的函数和异常等
（如 print、len、int 等）
'''

'''
全局命名空间（Global Namespace）
每个模块（.py 文件）都会有一个属于自己的全局命名空间，
存放该模块中定义的变量、函数、类等
'''

'''
局部命名空间（Local Namespace）
每次调用函数时都会创建一个局部命名空间，存放函数内部定义的变量和参数。
命名空间的查找顺序遵循“LEGB”原则：
L（Local）：局部命名空间
E（Enclosing）：外部嵌套函数的命名空间（闭包）
G（Global）：全局命名空间
B（Built-in）：内置命名空间
'''

'''
命名空间查找顺序 
局部命名空间 >> 全局命名空间 >> 内置命名空间
'''
x = 10  # 全局命名空间

def func():
    y = 20  # 局部命名空间
    print(x)  # 先在局部找x，找不到再去全局找

func()